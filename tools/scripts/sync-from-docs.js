#!/usr/bin/env node

/**
 * Sync skills and CLI (pillars) from core framework docs.
 * Run manually: node tools/scripts/sync-from-docs.js
 * Or via npm run sync:from-docs. Triggered automatically when docs change (see install-docs-sync-hook.js).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { CROSS_CUTTING_PILLARS, DOC15_PILLAR_MAP } from "./sync-config.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "../..");
const DOCS_DIR = path.join(REPO_ROOT, "docs");
const PILLARS_OUT = path.join(REPO_ROOT, "tools", "aaf-posture", "pillars.js");
const SKILL_ARCH_REVIEW = path.join(REPO_ROOT, "tools", "skills", "aaf-architecture-review", "SKILL.md");

function readDoc(name) {
  const p = path.join(DOCS_DIR, name);
  try {
    return fs.readFileSync(p, "utf8");
  } catch (e) {
    console.warn("Warning: could not read", p, e.message);
    return "";
  }
}

/**
 * Parse doc 15 section 14.2 (Mode 2: Architecture Reviews) into pillar id -> list of question strings.
 */
function parseDoc15ReviewSection(content) {
  const startMarker = "### **14.2 Mode 2: Architecture Reviews";
  const endMarker = "### **14.3";
  const start = content.indexOf(startMarker);
  const end = content.indexOf(endMarker, start);
  if (start === -1 || end === -1) {
    console.warn("Warning: could not find 14.2 or 14.3 in doc 15");
    return {};
  }
  const block = content.slice(start, end);
  const lines = block.split(/\r?\n/);
  const result = {};
  let current = null;
  for (const line of lines) {
    const trimmed = line.trim();
    if (DOC15_PILLAR_MAP[trimmed]) {
      current = DOC15_PILLAR_MAP[trimmed].id;
      result[current] = [];
      continue;
    }
    const bullet = trimmed.match(/^\*\s+(.+)$/);
    if (bullet && current) {
      result[current].push(bullet[1].trim());
    }
  }
  return result;
}

function buildAllPillars(sixPillarsFromDoc) {
  const pillars = [];
  const order = [
    "security",
    "reliability",
    "cost",
    "operational-excellence",
    "performance",
    "sustainability",
  ];
  for (const id of order) {
    const qs = sixPillarsFromDoc[id];
    const meta = Object.values(DOC15_PILLAR_MAP).find((m) => m.id === id);
    if (meta && qs && qs.length) {
      pillars.push({ id: meta.id, name: meta.name, questions: qs });
    }
  }
  pillars.push(...CROSS_CUTTING_PILLARS);
  return pillars;
}

function buildPillarsJs(pillars) {
  const lines = [
    "/**",
    " * Pillar definitions: id, name, and review questions (from AAF application method).",
    " * Used by the CLI to structure the posture report.",
    " * Auto-generated by tools/scripts/sync-from-docs.js — do not edit by hand.",
    " */",
    "export const PILLARS = [",
  ];
  for (const p of pillars) {
    lines.push("  {");
    lines.push(`    id: "${p.id}",`);
    lines.push(`    name: "${p.name.replace(/"/g, '\\"')}",`);
    lines.push("    questions: [");
    for (const q of p.questions) {
      lines.push(`      "${q.replace(/"/g, '\\"')}",`);
    }
    lines.push("    ],");
    lines.push("  },");
  }
  lines.push("];");
  return lines.join("\n") + "\n";
}

/**
 * Update the "## Mode 2: Architecture review" section in aaf-architecture-review/SKILL.md
 * with checklist items derived from the same pillar data.
 */
function updateArchitectureReviewSkillMode2(pillars) {
  let content;
  try {
    content = fs.readFileSync(SKILL_ARCH_REVIEW, "utf8");
  } catch {
    return;
  }
  const sectionStart = "## Mode 2: Architecture review (pre-production)";
  const sectionEnd = "## Maturity model (scaling autonomy safely)";
  const i = content.indexOf(sectionStart);
  const j = content.indexOf(sectionEnd, i);
  if (i === -1 || j === -1) return;
  let newSection = sectionStart + "\n\nApply pillars as structured review lenses. Absence of budgets, verification, and observability is a strong indicator the system is not production-ready.\n\n";
  for (const p of pillars) {
    const heading = p.name.replace(" (cross-cutting)", "").trim();
    newSection += `**${heading}**\n\n`;
    for (const q of p.questions) {
      newSection += `- [ ] ${q}\n`;
    }
    newSection += "\n";
  }
  const before = content.slice(0, i);
  const after = content.slice(j);
  const updated = before + newSection + after;
  fs.writeFileSync(SKILL_ARCH_REVIEW, updated, "utf8");
  console.log("Updated", path.relative(REPO_ROOT, SKILL_ARCH_REVIEW), "(Mode 2 section)");
}

function main() {
  const doc15 = readDoc("15-application-method.md");
  const sixPillars = parseDoc15ReviewSection(doc15);
  const allPillars = buildAllPillars(sixPillars);
  const pillarsJs = buildPillarsJs(allPillars);

  fs.writeFileSync(PILLARS_OUT, pillarsJs, "utf8");
  console.log("Wrote", path.relative(REPO_ROOT, PILLARS_OUT));

  updateArchitectureReviewSkillMode2(allPillars);
}

main();
